export function ChannelIcon({ source, className = "size-4" }: { source: string; className?: string }) {
  switch (source) {
    case "INSTAGRAM":
      return <InstagramGlyph className={cn(className, "text-[#E4405F]")} />;
    case "WHATSAPP":
      return <WhatsappGlyph className={cn(className, "text-[#25D366]")} />;
    case "FACEBOOK":
      return <FacebookGlyph className={cn(className, "text-[#1877F2]")} />;
    case "CONFIGURATEUR":
      return <GoogleGlyph className={className} />;
    case "EMAIL":
      return <Mail className={cn(className, "text-zinc-500")} />;
    case "SMS":
      return <MessageSquare className={cn(className, "text-zinc-500")} />;
    case "TELEPHONE":
      return <Phone className={cn(className, "text-zinc-500")} />;
    case "BOUCHE_A_OREILLE":
      return <Users className={cn(className, "text-zinc-500")} />;
    default:
      return <MessageCircle className={cn(className, "text-zinc-400")} />;
  }
}
